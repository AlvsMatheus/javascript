// Repetição com variável de controle

for(var contagem = 1; contagem <= 10; contagem++){
    console.log(contagem)
}

/*
For(início, teste, incremento)

início => é a variável ou o dado
teste => é o que você colocou como referência da 
repetição (número de repetições)
incremento => como a variável vai avançar (c++ = de um em um)

Nesse método o código, ao terminar, caso não
chegue ao número de repetições ele executa
o comando, adiciona mais um incremento e
retorna ao teste
*/ 